typedef  unsigned short int  u16 ;
typedef  unsigned char       u8  ;

typedef    signed int        s32 ; 
typedef    signed short int  s16 ;
typedef    signed char       s8 ;


#define   PORTA     (u8)0
#define   PORTB     (u8)1
#define   PORTC     (u8)2 
#define   PORTD     (u8)3
#define   PORTE     (u8)4
#define   PORTF     (u8)5 

#define   PIN0       (u8)0
#define   PIN1       (u8)1
#define   PIN2       (u8)2
#define   PIN3       (u8)3
#define   PIN4       (u8)4
#define   PIN5       (u8)5
#define   PIN6       (u8)6
#define   PIN7       (u8)7

#endif
